package po.BSJ;

public class BArticle {
    private int lc_id;
    private String title;
    private int book_id;
    private int author_id;
    private String lc_time;
    private String lc_article;
    private int lc_comment;
    private int click;

    public int getLc_id() {
        return lc_id;
    }

    public void setLc_id(int lc_id) {
        this.lc_id = lc_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getBook_id() {
        return book_id;
    }

    public void setBook_id(int book_id) {
        this.book_id = book_id;
    }

    public int getAuthor_id() {
        return author_id;
    }

    public void setAuthor_id(int author_id) {
        this.author_id = author_id;
    }

    public String getLc_time() {
        return lc_time;
    }

    public void setLc_time(String lc_time) {
        this.lc_time = lc_time;
    }

    public String getLc_article() {
        return lc_article;
    }

    public void setLc_article(String lc_article) {
        this.lc_article = lc_article;
    }

    public int getLc_comment() {
        return lc_comment;
    }

    public void setLc_comment(int lc_comment) {
        this.lc_comment = lc_comment;
    }

    public int getClick() {
        return click;
    }

    public void setClick(int click) {
        this.click = click;
    }
}
