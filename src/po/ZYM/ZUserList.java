package po.ZYM;

public class ZUserList {
    private ZUser fansList;
    private int countFans;
    private int count;

    public ZUser getFansList() {
        return fansList;
    }

    public void setFansList(ZUser fansList) {
        this.fansList = fansList;
    }

    public int getCountFans() {
        return countFans;
    }

    public void setCountFans(int countFans) {
        this.countFans = countFans;
    }
    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
